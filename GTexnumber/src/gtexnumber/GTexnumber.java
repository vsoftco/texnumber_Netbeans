    private void replace_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_replace_buttonActionPerformed
        // TODO add your handling code here:
        File in = new File(input_text.getText());
        File out = new File(output_text.getText());

        String output_file = new String();

        if (in.getAbsolutePath().compareTo(out.getAbsolutePath()) == 0) // Test to see if it's the same output file
        {
            output_file = out.getAbsolutePath() + "tmp";
        } else {
            output_file = out.getAbsolutePath();
        }

        try {
            Operations op = new Operations(in.getAbsolutePath(), output_file,
                    pattern_text.getText(), replacement_text.getText());
            op.ReplaceReferences();
            log_text.setText("");
            op.OutputLog(log_text);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (in.getAbsolutePath().compareTo(out.getAbsolutePath()) == 0) // We have the same output
        // Re-write the input with the output
        {
            // First create a backup of the input file
            try {
                FileReader finbak = new FileReader(in.getAbsolutePath());
                FileWriter foutbak = new FileWriter(in.getAbsolutePath() + "bak"); // backup

                Scanner srcbak = new Scanner(finbak);


                while (srcbak.hasNext()) // Read the file line by line
                {
                    // Write the results to the output file, line by line
                    foutbak.write(srcbak.nextLine() + System.getProperty("line.separator"));
                }

                finbak.close();
                foutbak.close();
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }


            // Then replace the input file with the output file
            try {
                FileReader fin = new FileReader(out.getAbsolutePath() + "tmp");
                FileWriter fout = new FileWriter(in.getAbsolutePath());

                Scanner src = new Scanner(fin);


                while (src.hasNext()) // Read the file line by line
                {
                    // Write the results to the output file, line by line
                    fout.write(src.nextLine() + System.getProperty("line.separator"));
                }

                fin.close();
                fout.close();

                // Finaly delete the temp output
                File fdel = new File(out.getAbsolutePath() + "tmp");
                fdel.delete();
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

        }
    }//GEN-LAST:event_replace_buttonActionPerformed

    private void open_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_open_buttonActionPerformed
        // TODO add your handling code here:
        // Create a filter for .tex files
        class MyFilter extends javax.swing.filechooser.FileFilter {

            public boolean accept(File file) {
                return file.isDirectory() || file.getName().endsWith(".tex");
            }

            public String getDescription() {
                return "TeX files (*.tex)";
            }
        }


        final JFileChooser fc = new JFileChooser(current_directory);
        fc.addChoosableFileFilter(new MyFilter()); // Add the filter

        int returnVal = fc.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            input_text.setText(fc.getSelectedFile().getAbsolutePath());
            output_text.setText(fc.getSelectedFile().getParent() + System.getProperty("file.separator") + "renum.tex");
            current_directory = fc.getCurrentDirectory();
        }
    }//GEN-LAST:event_open_buttonActionPerformed

